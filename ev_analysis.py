summary = df.groupby('Station_Name').agg({
    'Waiting_Time': 'mean',
    'Distance_km': 'mean',
    'Rating': 'mean',
    'Queue_Length': 'mean'
}).reset_index()
print(summary)

summary['Score'] = (
    (1 / summary['Distance_km']) * 0.4 +
    (1 / summary['Waiting_Time']) * 0.3 +
    (summary['Rating']) * 0.3
)
print(summary.sort_values(by='Score', ascending=False))

