import folium

# Create base map (centered around Chennai)
m = folium.Map(location=[13.0827, 80.2707], zoom_start=11)

# Function to assign color based on queue
def get_color(queue):
    if queue <= 2:
        return 'green'
    elif queue <= 5:
        return 'orange'
    else:
        return 'red'

# Add points to map
for i, row in df.iterrows():
    folium.CircleMarker(
        location=[row['Latitude'], row['Longitude']],
        radius=6,
        color=get_color(row['Queue_Length']),
        fill=True,
        fill_color=get_color(row['Queue_Length']),
        fill_opacity=0.7,
        popup=f"""
        Station: {row['Station_Name']}<br>
        Location: {row['Location']}<br>
        Queue: {row['Queue_Length']}<br>
        Energy: {row['Energy_Consumed_kWh']} kWh<br>
        Slots: {row['Slots']}<br>
        Vehicles: {row['Vehicles_Count']}
        """
    ).add_to(m)

# Show map
m.save("ev_map.html")

